Служение Обществу: Действовать в интересах поселения, а не только своей личной выгоды. Эгоистичные решения могут привести к народному гневу.<br>
Нести Ответственность: Быть готовым держать ответ за последствия принятых законов перед избирателями и, что немаловажно, перед Мастером.<br>
Соблюдение Иерархии: Признавать, что окончательное слово в толковании правил, эффектов и последствий всегда остается за Мастером.<br>
            
