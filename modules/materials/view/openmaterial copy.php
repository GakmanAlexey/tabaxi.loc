                <h3 class="tut_006_section_subtitle">От идеи до указа: жизненный цикл закона</h3>
                <p class="tut_006_text">
Каждый закон проходит через следующие стадии:<br>
<br>1. Инициатива:<br>Любой игрок (не только Управленец) может предложить идею для нового закона. Для этого он должен изложить ее в установленном формате ) и передать на рассмотрение одному из членов Совета. Это поощряет политическое участие всех жителей.
<br>2. Обсуждение в Совете:<br>Управленцы изучают предложение в специально отведенном для этого месте. Они задают вопросы автору, обсуждают потенциальные эффекты, слабые места и возможные компромиссы. Мастер на этом этапе выступает в роли эксперта, помогая оценить возможные игровые последствия.
<br>3. Голосование:<br>После обсуждения проводится формальное голосование среди трех Управленцев. Для принятия закона требуется большинство голосов. Голосование является окончательным.
<br>4. Обнародование:<br>Принятый закон передается Мастеру, который официально объявляет о нем всем игрокам через основной канал мира. Объявление должно содержать полный текст закона и дату, с которой он вступает в силу.
<br>5. Вступление в силу:<br>Закон начинает действовать либо немедленно после обнародования, либо с указанной в нем даты (например, «с начала следующей игровой недели»).
<br>6. Исполнение и последствия:<br>Мастер интегрирует эффекты закона в игру. Он отслеживает изменения в показателях поселения (казны, настроения и т.д.) и отыгрывает реакцию мира.         
                </p>
